namespace MarkdownDemo
{
    /// <summary>
    /// # Задание: Демонстрация Markdown в комментариях
    /// 
    /// Это консольное приложение демонстрирует **различные форматы** 
    /// *Markdown-разметки* в XML-комментариях к коду.
    /// 
    /// ## Основные возможности:
    /// 1. **Жирный текст** — для важных замечаний
    /// 2. *Курсив* — для акцентов
    /// 3. ***Жирный курсив*** — для особого выделения
    /// 4. ~~Зачёркнутый~~ — для устаревшей информации
    /// 5. `Код` — для упоминания переменных и методов
    /// 
    /// ### Пример кода в комментарии:
    /// ```
    /// double sum = number1 + number2;
    /// Console.WriteLine($"Сумма: {sum}");
    /// ```
    /// 
    /// > **Примечание:** Программа запрашивает у пользователя два числа
    /// > и выводит результат их сложения.
    /// </summary>
    class Program
    {
        static void Main(string[] args)
        {
            // ## Этап 1: Ввод первого числа
            // Пользователь вводит *первое слагаемое* с клавиатуры
            Console.WriteLine("Введите первое число: ");
            double number1 = Convert.ToDouble(Console.ReadLine());

            // --------------------------------------------------------------
            
            // ## Этап 2: Ввод второго числа
            // Пользователь вводит *второе слагаемое* с клавиатуры
            Console.WriteLine("Введите второе число: ");
            double number2 = Convert.ToDouble(Console.ReadLine());

            // --------------------------------------------------------------
            
            // ## Этап 3: Вычисление суммы
            // Производим операцию **сложения** двух чисел:
            // `double sum = number1 + number2;`
            double sum = number1 + number2;

            // --------------------------------------------------------------
            
            // ## Этап 4: Вывод результата
            // ~~Старый способ:~~ Console.WriteLine("Результат: " + sum);
            // Используем **интерполяцию строк**:
            Console.WriteLine($"Результат операции: {sum}");
            
            // --------------------------------------------------------------
            
            // ### Дополнительная информация:
            // - Первое число: `{number1}`
            // - Второе число: `{number2}`
            // - ~~Сумма в целочисленном формате:~~ (не требуется)
            // - ***Сумма в текущем формате:*** `{sum}`
            
            Console.WriteLine("\nНажмите любую клавишу для выхода...");
            Console.ReadKey();
        }
    }
}
